    private void btnhasilActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnhasilActionPerformed
        // TODO add your handling code here:
        switch(pilih){
        case 1:
        bil2=Double.parseDouble(bil);
        jumlah=bil1+bil2;
        bil=Double.toString(jumlah);
        hasil.setText(bil);
        break;
        
        case 2:
        bil2=Double.parseDouble(bil);
        jumlah=bil1-bil2;
        bil=Double.toString(jumlah);
        hasil.setText(bil);
        break;
        
        case 3:
        bil2=Double.parseDouble(bil);
        jumlah=bil1*bil2;
        bil=Double.toString(jumlah);
        hasil.setText(bil);
        break;
        
        case 4:
        bil2=Double.parseDouble(bil);
        jumlah=bil1/bil2;
        bil=Double.toString(jumlah);
        hasil.setText(bil);
        break;
        
        case 5:
        jumlah=Math.sqrt(bil1);
        bil=Double.toString(jumlah);
        hasil.setText(bil);
        break;
        
        case 6:
        jumlah=bil1/100;
        bil=Double.toString(jumlah);
        hasil.setText(bil);
        break;
        
        case 7:
        bil2=Double.parseDouble(bil);
        jumlah=Math.pow(bil1, bil2);
        bil=Double.toString(jumlah);
        hasil.setText(bil);
        break;
        
        case 8:
            bil2=Double.parseDouble(bil);
        jumlah=bil1 % bil2;
        bil=Double.toString(jumlah);
        hasil.setText(bil);
        break;
        
        
        }
        
    }//GEN-LAST:event_btnhasilActionPerformed

